// ── Inject partLibrary members into functions ────────────────────────────────
const libConst = constants.find((c) => c.name === "partLibrary");
if (libConst) {
  for (const m of libConst.members) {
    if (m.signature.includes("(")) {
      functions.push({ name: `lib.${m.name}`, signature: `lib.${m.signature}`, jsdoc: m.jsdoc });
    }
  }
}

// ── Build lookup maps ────────────────────────────────────────────────────────

const catByTitle = new Map(CATEGORIES.map((c) => [c.title, c]));
const fnNameToCategory = new Map();
for (const cat of CATEGORIES) {
  for (const n of cat.names) fnNameToCategory.set(n, cat.title);
}

const fnsByCategory = new Map();
const uncategorizedFns = [];
for (const fn of functions) {
  const catTitle = fnNameToCategory.get(fn.name);
  if (catTitle) {
    if (!fnsByCategory.has(catTitle)) fnsByCategory.set(catTitle, []);
    fnsByCategory.get(catTitle).push(fn);
  } else {
    uncategorizedFns.push(fn);
  }
}

// ── Render helpers ───────────────────────────────────────────────────────────

function renderFunction(fn) {
  const out = [];
  out.push(`#### \`${fn.name}()\``);
  out.push("");
  out.push("```ts");
  out.push(simplifySig(fn.signature));
  out.push("```");
  if (fn.jsdoc) { out.push(""); out.push(fn.jsdoc); }
  out.push("");
  return out.join("\n");
}

function renderClass(cls) {
  const out = [];
  out.push(`### \`${cls.name}\``);
  out.push("");
  if (cls.jsdoc) { out.push(cls.jsdoc); out.push(""); }

  const seenMethods = new Set();
  const uniqueMethods = cls.methods.filter((m) => {
    if (m.name.startsWith("_") || seenMethods.has(m.name)) return false;
    seenMethods.add(m.name);
    return true;
  });

  const pubProps = cls.properties.filter((p) => !p.name.startsWith("_") && p.name !== "colorHex");
  if (pubProps.length > 0) {
    out.push("**Properties:**");
    out.push("");
    out.push("| Property | Type | Description |");
    out.push("|----------|------|-------------|");
    for (const prop of pubProps) {
      const type = prop.type.length > 50 ? prop.type.slice(0, 47) + "..." : prop.type;
      out.push(`| \`${prop.name}\` | \`${type}\` | ${prop.jsdoc || "—"} |`);
    }
    out.push("");
  }

  if (uniqueMethods.length > 0) {
    out.push("**Methods:**");
    out.push("");
    for (const method of uniqueMethods) {
      const prefix = method.isStatic ? "static " : method.isGetter ? "get " : "";
      const desc = method.jsdoc || simplifySig(method.signature).slice(0, 80);
      out.push(`- \`${prefix}${method.name}()\` — ${desc}`);
    }
    out.push("");
  }

  return out.join("\n");
}

function renderConst(c) {
  const out = [];
  out.push(`### \`${c.name}\``);
  out.push("");
  if (c.jsdoc) { out.push(c.jsdoc); out.push(""); }
  if (c.members.length > 0) {
    out.push("**Members:**");
    out.push("");
    for (const m of c.members) {
      const desc = m.jsdoc || "";
      out.push(`- \`${m.name}()\` — ${desc || simplifySig(m.signature).slice(0, 80)}`);
    }
    out.push("");
  }
  return out.join("\n");
}

// ── Render a module file ─────────────────────────────────────────────────────

function renderModule(mod) {
  const out = [];
  out.push(`# ${mod.title}`);
  out.push("");
  out.push(`> **Auto-generated** from \`src/forge/forge-public-api.ts\`. Do not edit by hand — run \`npm run gen:docs\` to regenerate.`);
  out.push("");
  out.push(mod.desc);
  out.push("");

  const catFnPairs = mod.categoryTitles
    .map((t) => ({ cat: catByTitle.get(t), fns: fnsByCategory.get(t) || [] }))
    .filter(({ fns }) => fns.length > 0);

  if (catFnPairs.length > 0) {
    out.push("## Functions");
    out.push("");
    for (const { cat, fns } of catFnPairs) {
      out.push(`### ${cat.title}`);
      out.push("");
      if (cat.desc) { out.push(cat.desc); out.push(""); }
      for (const fn of fns) out.push(renderFunction(fn));
    }
  }

  // Uncategorized functions go into core.md only
  if (mod.slug === "core" && uncategorizedFns.length > 0) {
    out.push("### Other");
    out.push("");
    for (const fn of uncategorizedFns) out.push(renderFunction(fn));
  }

  const clsEntries = mod.classNames.map((n) => classMap.get(n)).filter(Boolean);
  if (clsEntries.length > 0) {
    out.push("---");
    out.push("");
    out.push("## Classes");
    out.push("");
    for (const cls of clsEntries) out.push(renderClass(cls));
  }

  const constEntries = mod.constNames
    .map((n) => constants.find((c) => c.name === n))
    .filter(Boolean)
    .filter((c) => c.name !== "partLibrary"); // lib.* members already rendered as functions

  if (constEntries.length > 0) {
    out.push("---");
    out.push("");
    out.push("## Constants");
    out.push("");
    for (const c of constEntries) out.push(renderConst(c));
  }

  return out.join("\n").trimEnd() + "\n";
}

// ── Main ─────────────────────────────────────────────────────────────────────

mkdirSync(outDir, { recursive: true });

// Any class not explicitly claimed by a module falls into core.md
const claimedClasses = new Set(MODULES.flatMap((m) => m.classNames));
const unclaimedClasses = [...classMap.keys()].filter((n) => !claimedClasses.has(n));
if (unclaimedClasses.length > 0) {
  MODULES.find((m) => m.slug === "core").classNames.push(...unclaimedClasses);
  console.log(`  (unclaimed classes → core.md: ${unclaimedClasses.join(", ")})`);
}

for (const mod of MODULES) {
  const markdown = renderModule(mod);
  const outPath = resolve(outDir, `${mod.slug}.md`);
  writeFileSync(outPath, markdown);
  const fnCount = mod.categoryTitles.reduce((s, t) => s + (fnsByCategory.get(t)?.length ?? 0), 0);
  const extraFns = mod.slug === "core" ? uncategorizedFns.length : 0;
  console.log(`  ${mod.slug}.md — ${fnCount + extraFns} fns, ${mod.classNames.length} classes`);
}

console.log(`\nGenerated ${MODULES.length} files in docs/permanent/generated/`);
console.log(`Total: ${functions.length} functions, ${classMap.size} classes, ${constants.length} constants`);
