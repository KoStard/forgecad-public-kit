#!/usr/bin/env node
/**
 * Generates per-module API reference docs under docs/permanent/generated/
 * from the TypeScript public API source file (forge-public-api.ts) via the
 * bundled .d.ts output.
 *
 * Outputs:
 *   docs/permanent/generated/core.md          — primitives, booleans, patterns, imports, params
 *   docs/permanent/generated/sketch.md        — 2D sketches and constrained sketches
 *   docs/permanent/generated/assembly.md      — assembly, joints, couplings
 *   docs/permanent/generated/curves.md        — curves and surfacing
 *   docs/permanent/generated/sheet-metal.md   — sheet metal operations
 *   docs/permanent/generated/viewport.md      — runtime viewport APIs
 *   docs/permanent/generated/output.md        — annotations, dimensions, export
 *   docs/permanent/generated/lib.md           — part library (fasteners, gears, pipes, etc.)
 *
 * Run:  node scripts/gen-api-docs.mjs
 *   or: npm run gen:docs
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const dtsPath = resolve(root, "src/forge/forge-api.d.ts");
const outDir = resolve(root, "docs/permanent/generated");

// ── Parse the .d.ts file ────────────────────────────────────────────────────

const content = readFileSync(dtsPath, "utf-8");
const lines = content.split("\n");

function collectBlock(start) {
  let depth = 0;
  for (let i = start; i < lines.length; i++) {
    for (const ch of lines[i]) {
      if (ch === "{") depth++;
      if (ch === "}") depth--;
    }
    if (depth <= 0) return i;
  }
  return lines.length - 1;
}

function collectDeclaration(start) {
  let depth = 0;
  const collected = [];
  for (let i = start; i < lines.length; i++) {
    const ln = lines[i];
    collected.push(ln);
    for (const ch of ln) {
      if (ch === "{") depth++;
      if (ch === "}") depth--;
    }
    if (depth <= 0 && ln.trimEnd().endsWith(";")) {
      return [collected.join("\n"), i];
    }
  }
  return [collected.join("\n"), lines.length - 1];
}

function parseJsdoc(text) {
  if (!text) return "";
  return text
    .replace(/\/\*\*/, "")
    .replace(/\*\//, "")
    .split("\n")
    .map((l) => l.replace(/^\s*\*\s?/, "").trim())
    .filter((l) => l && !l.startsWith("@"))
    .join(" ")
    .trim();
}

function compactSig(raw) {
  return raw.replace(/\n\s*/g, " ").replace(/\s{2,}/g, " ").replace(/;\s*$/, "").trim();
}

function simplifySig(sig) {
  let result = sig;
  for (let pass = 0; pass < 4; pass++) {
    result = result.replace(/\{([^{}]*)\}/g, (match) => {
      if (match.length > 100) return "{ ... }";
      return match;
    });
  }
  return result;
}

// ── Main parser ─────────────────────────────────────────────────────────────

const functions = [];
const classMap = new Map();
const constants = [];

let pendingJsdoc = "";
let i = 0;

while (i < lines.length) {
  const line = lines[i];
  const trimmed = line.trim();

  if (trimmed.startsWith("// External type stubs") || trimmed.startsWith("// Generated by")) {
    pendingJsdoc = "";
    i++;
    continue;
  }

  if (/^type \w+ = unknown;$/.test(trimmed)) {
    i++;
    continue;
  }

  if (trimmed.startsWith("/**")) {
    const jsdocLines = [];
    while (i < lines.length) {
      jsdocLines.push(lines[i]);
      if (lines[i].includes("*/")) { i++; break; }
      i++;
    }
    pendingJsdoc = parseJsdoc(jsdocLines.join("\n"));
    continue;
  }

  if (line.startsWith("declare function ")) {
    const [fullText, endIdx] = collectDeclaration(i);
    const nameMatch = fullText.match(/^declare function (\w+)/);
    if (nameMatch && !nameMatch[1].startsWith("_")) {
      const sig = compactSig(fullText.replace(/^declare function /, ""));
      functions.push({ name: nameMatch[1], signature: sig, jsdoc: pendingJsdoc });
    }
    pendingJsdoc = "";
    i = endIdx + 1;
    continue;
  }

  if (line.startsWith("declare class ") || line.startsWith("declare abstract class ")) {
    const nameMatch = line.match(/class (\w+)/);
    if (nameMatch) {
      const clsName = nameMatch[1];
      const endIdx = collectBlock(i);
      const body = lines.slice(i + 1, endIdx);
      const parsed = parseClassBody(body);
      if (!classMap.has(clsName)) {
        classMap.set(clsName, { name: clsName, jsdoc: pendingJsdoc, methods: [], properties: [] });
      }
      const cls = classMap.get(clsName);
      if (pendingJsdoc && !cls.jsdoc) cls.jsdoc = pendingJsdoc;
      cls.methods.push(...parsed.methods);
      cls.properties.push(...parsed.properties);
      i = endIdx + 1;
    } else {
      i++;
    }
    pendingJsdoc = "";
    continue;
  }

  if (line.startsWith("interface ") && !line.startsWith("interface ConstraintTypeMap")) {
    const ifMatch = line.match(/^interface (\w+)/);
    if (ifMatch) {
      const ifName = ifMatch[1];
      const endIdx = collectBlock(i);
      if (classMap.has(ifName) || ["Shape", "TrackedShape", "ConstrainedSketchBuilder"].includes(ifName)) {
        const body = lines.slice(i + 1, endIdx);
        const parsed = parseClassBody(body);
        if (!classMap.has(ifName)) {
          classMap.set(ifName, { name: ifName, jsdoc: "", methods: [], properties: [] });
        }
        const cls = classMap.get(ifName);
        cls.methods.push(...parsed.methods);
      }
      i = endIdx + 1;
    } else {
      i++;
    }
    pendingJsdoc = "";
    continue;
  }

  if (line.startsWith("declare const ") && !line.includes("= unknown") && !line.includes("unique symbol")) {
    const constMatch = line.match(/^declare const (\w+)/);
    if (constMatch && !constMatch[1].startsWith("_") && constMatch[1] !== "SHAPE_BACKEND_MARKER") {
      const endIdx = collectBlock(i);
      const body = lines.slice(i + 1, endIdx);
      const parsed = parseConstBody(body);
      constants.push({ name: constMatch[1], jsdoc: pendingJsdoc, members: parsed });
      i = endIdx + 1;
    } else {
      i++;
    }
    pendingJsdoc = "";
    continue;
  }

  pendingJsdoc = "";
  i++;
}

// ── Class body parser ───────────────────────────────────────────────────────

function parseClassBody(bodyLines) {
  const methods = [];
  const properties = [];
  let doc = "";
  let j = 0;

  while (j < bodyLines.length) {
    const bl = bodyLines[j].trim();

    if (bl.startsWith("/**")) {
      const jdLines = [];
      while (j < bodyLines.length) {
        jdLines.push(bodyLines[j]);
        if (bodyLines[j].includes("*/")) { j++; break; }
        j++;
      }
      doc = parseJsdoc(jdLines.join("\n"));
      continue;
    }

    if (!bl || bl === "}" || bl.startsWith("private ")) { doc = ""; j++; continue; }

    if (bl.startsWith("constructor(")) {
      let depth = 0;
      while (j < bodyLines.length) {
        for (const ch of bodyLines[j]) { if (ch === "(") depth++; if (ch === ")") depth--; }
        j++;
        if (depth <= 0 && bodyLines[j - 1].trimEnd().endsWith(";")) break;
      }
      doc = "";
      continue;
    }

    const methodMatch = bl.match(/^(?:(static|get)\s+)?(\w+)\s*(<[^>]*>)?\s*\(/);
    if (methodMatch) {
      const isStatic = methodMatch[1] === "static";
      const isGetter = methodMatch[1] === "get";
      const name = methodMatch[2];
      let fullSig = bl;
      let depth = 0;
      for (const ch of bl) { if (ch === "{") depth++; if (ch === "}") depth--; }
      while (depth > 0 || !fullSig.trimEnd().endsWith(";")) {
        j++;
        if (j >= bodyLines.length) break;
        fullSig += " " + bodyLines[j].trim();
        for (const ch of bodyLines[j]) { if (ch === "{") depth++; if (ch === "}") depth--; }
      }
      methods.push({ name, signature: compactSig(fullSig), jsdoc: doc, isStatic, isGetter });
      doc = "";
      j++;
      continue;
    }

    if (bl.includes(":") && !bl.includes("(") && bl.endsWith(";")) {
      const propMatch = bl.match(/^(?:readonly\s+)?(\w+)\??\s*:\s*(.+);$/);
      if (propMatch) properties.push({ name: propMatch[1], type: propMatch[2], jsdoc: doc });
      doc = "";
      j++;
      continue;
    }

    doc = "";
    j++;
  }

  return { methods, properties };
}

function parseConstBody(bodyLines) {
  const members = [];
  let doc = "";

  for (let j = 0; j < bodyLines.length; j++) {
    const bl = bodyLines[j].trim();
    if (bl.startsWith("/**")) {
      const jdLines = [];
      while (j < bodyLines.length) {
        jdLines.push(bodyLines[j]);
        if (bodyLines[j].includes("*/")) { j++; break; }
        j++;
      }
      doc = parseJsdoc(jdLines.join("\n"));
      j--;
      continue;
    }

    const mMatch = bl.match(/^(\w+)\s*[:(]/);
    if (mMatch) {
      let fullSig = bl;
      let depth = 0;
      for (const ch of bl) { if (ch === "{") depth++; if (ch === "}") depth--; }
      while (depth > 0 || (!fullSig.trimEnd().endsWith(";") && !fullSig.trimEnd().endsWith(","))) {
        j++;
        if (j >= bodyLines.length) break;
        fullSig += " " + bodyLines[j].trim();
        for (const ch of bodyLines[j]) { if (ch === "{") depth++; if (ch === "}") depth--; }
      }
      members.push({ name: mMatch[1], signature: compactSig(fullSig), jsdoc: doc });
      doc = "";
    } else {
      doc = "";
    }
  }

  return members;
}

